/**
 * FAQ 데이터를 Gemini 프롬프트용 텍스트로 변환
 */
export function buildFaqPromptText(): string {
    return FAQ_ITEMS.map((item) =>
        `[FAQ ID: ${item.id}]\nQ: ${item.question}\nA: ${item.answer.join(" ")}`
    ).join("\n\n");
}
